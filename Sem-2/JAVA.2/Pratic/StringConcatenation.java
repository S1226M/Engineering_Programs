public class StringConcatenation {
    public static void main(String[] args) {
        // Define two strings
        String str1 = "Hello";
        String str2 = "World";
        
        // Concatenate the strings
        String result = str1 + " " + str2;
        
        // Print the concatenated string
        System.out.println("Concatenated string: " + result);
    }
}
